module.exports = function getLocation(ip: string): string {
  const geo = geoip.lookup(ip);

  if (typeof geo === 'object' && geo !== null) {
    let location = geo.city;

    if (Object.prototype.hasOwnProperty.call(ISO_CODE_REL_COUNTRY, geo.country)) {
      location += `, ${ISO_CODE_REL_COUNTRY[geo.country]}`;
    }

    return location;
  }

  return 'Unknown';
};
